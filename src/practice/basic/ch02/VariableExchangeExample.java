package practice.basic.ch02;

public class VariableExchangeExample {
    public static void main(String[] args) {
        int x = 3;
        int y = 5;
        System.out.println("x: " +x +" ," +"y: " +y );
        x = 5;
        y = 3;
        System.out.println("x: " +x +", "+ "y: " +y );
    }
}
